// // OBS: O ($) é o que permite interpolação de string.
// // Interpolação é um jeit mais fácil e limpo de inserir variáveis dentro de uma string.

// // Exemplo
// // string nome = "Paulo";
// // Console.WriteLine("Seu nome é " + nome); // concatenação tradicional

// // string nome = "Paulo";
// // Console.WriteLine($"Seu nome é {nome}"); // interpolação


// // Por que usar interpolação ($) é melhor?
// // Fica mais legível.

// // Evita ficar somando (+) strings.

// // Permite usar expressões direto dentro das chaves:


// // int idade = 25;
// // Console.WriteLine($"Ano que vem você terá {idade + 1} anos");
// // Sem $, você teria que fazer assim:


// // Console.WriteLine("Ano que vem você terá " + (idade + 1) + " anos");

// // Dica:
// // Sempre que for imprimir variáveis ou fazer contas no meio do texto, usa o $"" com {}. Seu código fica muito mais limpo e profissional.

